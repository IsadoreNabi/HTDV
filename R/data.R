#' Aggregated Output of the HTDV Factorial Simulation Study
#'
#' Sign-corrected aggregated results of the factorial Monte Carlo study
#' described in Section 12-bis of the companion paper. The study crosses
#' five factors at four levels each (sample size, AR(1) coefficient,
#' innovation tail, imbalance ratio, location shift) for 1024 cells, each
#' replicated 500 times against three inferential layers (HAR, bootstrap,
#' hierarchical Bayesian). One cell (n=500, phi=0.85, t3, imb=1, delta=0)
#' was lost to an I/O collision and is excluded; the table contains
#' 1023 cells x 3 layers = 3069 rows.
#'
#' @format A data frame with one row per (cell, layer) and the following
#' columns:
#' \describe{
#'   \item{n}{Per-group primary sample size (40, 80, 200, 500).}
#'   \item{phi}{AR(1) coefficient (0, 0.3, 0.6, 0.85).}
#'   \item{tail}{Innovation distribution: \code{"normal"}, \code{"t5"},
#'     \code{"t3"}, \code{"t2_1"}.}
#'   \item{imb}{Imbalance ratio n1/n2 (1, 1.5, 3, 6).}
#'   \item{delta}{Location shift in units of sigma_inf (0, 0.1, 0.25, 0.5).}
#'   \item{layer}{Inferential layer: \code{"har"}, \code{"boot"},
#'     \code{"bayes"}.}
#'   \item{n_reps}{Number of replications aggregated (500 for all cells).}
#'   \item{reject_rate}{Empirical rejection rate at nominal alpha = 0.05.}
#'   \item{coverage}{Fraction of replications whose 95\% interval covered
#'     the true parameter (sign-corrected for HAR and bootstrap).}
#'   \item{mean_ci_length}{Average length of the 95\% interval.}
#'   \item{mean_runtime}{Mean per-replication runtime in seconds.}
#'   \item{diag_pass_rate}{Fraction of replications passing all HMC
#'     diagnostic gates (Bayesian layer only; \code{NA} otherwise).}
#' }
#'
#' @source Generated by \code{run_simstudy.R} of the companion paper
#'   repository on master seed \code{20260422}, with sign-corrected
#'   coverage produced by \code{recover_coverage.R}. Total wall-clock
#'   31.1 hours on a 16-core Linux workstation. See
#'   \code{vignette("HTDV-validation")} for the full report.
"htdv_sim_summary"

#' HTDV Empirical Benchmarks Against Published References
#'
#' Output of the three-dataset external validation protocol of
#' Section 12-ter of the companion paper. Each dataset is run against a
#' pre-existing reference result from the published literature on the
#' same vintage, with the three HTDV inferential layers (HAR, bootstrap,
#' hierarchical Bayesian) reporting their point estimate, 95\% interval,
#' and a Bayesian diagnostic-pass flag.
#'
#' @format A data frame with three rows (one per dataset) and the
#' following columns:
#' \describe{
#'   \item{dataset}{Name of the benchmark dataset.}
#'   \item{reference}{Citation of the reference paper.}
#'   \item{n}{Sample size after preprocessing.}
#'   \item{sample}{Sample window (string).}
#'   \item{reference_value}{Reference point value.}
#'   \item{reference_se}{Reference standard error (\code{NA} when the
#'     reference does not provide one).}
#'   \item{har_point, har_ci_lo, har_ci_hi}{HAR-Wald point and 95\% CI.}
#'   \item{bayes_point, bayes_ci_lo, bayes_ci_hi}{Bayesian point and
#'     95\% credible interval.}
#'   \item{bayes_diag_pass, bayes_rhat_max, bayes_ess_min,
#'     bayes_divergences}{Bayesian HMC diagnostics.}
#'   \item{boot_point, boot_ci_lo, boot_ci_hi}{Stationary block bootstrap
#'     point and 95\% percentile CI.}
#'   \item{agreement_har, agreement_bayes, agreement_boot}{Per-layer
#'     agreement verdict: \code{"agreement"}, \code{"htdv-strict (above ref)"},
#'     or \code{"htdv-strict (below ref)"}.}
#' }
#'
#' @details
#' The three benchmarks (E1, E2, E3) and their references are:
#' \itemize{
#'   \item{\strong{E1.} Post-1984 US CPI inflation against Stock and
#'     Watson (2007). FRED-MD vintage 2026-03, variable \code{CPIAUCSL}
#'     transformed to annualized monthly inflation.}
#'   \item{\strong{E2.} Long-run mean of log-CAPE against Campbell and
#'     Shiller (1998). Robert Shiller's online historical dataset.}
#'   \item{\strong{E3.} US vs. Canada 10-year government bond yield
#'     differential against the iid Welch baseline. FRED series
#'     \code{GS10} and \code{IRLTLT01CAM156N}, common sample 1990-01 to
#'     latest.}
#' }
#'
#' @source Generated by \code{run_benchmarks.R} of the companion paper
#'   repository. See \code{vignette("HTDV-validation")} for the
#'   methodological narrative and the persistence-ladder reading.
"htdv_empirical_benchmarks"
